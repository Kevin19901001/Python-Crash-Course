# 使用2个乘号表示乘方运算
num = 3**2
print(num)

num = 3**3
print(num)

num = 0.2 + 0.1
print(num)

num = 3 * 0.1
print(num)


# 将任意2个数相除时，结果总是浮点数。无论哪种运算，只要有操作数是浮点数，python默认得到的总是浮点数。
num = 4/2
print(num)


# 数中的下划线
